package ec.edu.monster.model;

/**
 *
 * @author sebas
 */
public class Empleado {
     private String codigo;       // chr_emplcodigo
    private String paterno;      // vch_emplpaterno
    private String materno;      // vch_emplmaterno
    private String nombre;       // vch_emplnombre
    private String ciudad;       // vch_emplciudad
    private String direccion;    // vch_empldireccion

    // Getters y Setters
    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getPaterno() {
        return paterno;
    }

    public void setPaterno(String paterno) {
        this.paterno = paterno;
    }

    public String getMaterno() {
        return materno;
    }

    public void setMaterno(String materno) {
        this.materno = materno;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }
}
